class GenderDTO{


    public $id;

    public $slug;

    public $label;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param mixed $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }

    /**
     * @return mixed
     */
    public function getSlug()
    {
        return $this->slug;
    }

    /**
     * @param mixed $slug
     */
    public function setSlug($slug)
    {
        $this->slug = $slug;
    }





    public function convertToDTO($genderORM){
        $genderDTO = new GenderDTO();
        $genderDTO->setId($genderORM->id);
        $genderDTO->setLabel($genderORM->label);
        $genderDTO->setSlug($genderORM->slug);
        return $genderDTO;
    }




}