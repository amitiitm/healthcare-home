class LocalityDTO
{


    public $id;

    public $formattedAddress;

    public $json;

    /**
     * @return mixed
     */
    public function getFormattedAddress()
    {
        return $this->formattedAddress;
    }

    /**
     * @param mixed $formattedAddress
     */
    public function setFormattedAddress($formattedAddress)
    {
        $this->formattedAddress = $formattedAddress;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getJson()
    {
        return $this->json;
    }

    /**
     * @param mixed $json
     */
    public function setJson($json)
    {
        $this->json = $json;
    }






    public function convertToDto($locality){
        $localityDto = new LocalityDTO();
        $localityDto->setId($locality->id);
        $localityDto->setFormattedAddress($locality->formatted_address);
        $localityDto->setJson(json_decode($locality->json));
        return $localityDto;
    }



}