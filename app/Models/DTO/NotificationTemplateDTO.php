class NotificationTemplateDTO{


    public $id;

    public $label;

    public $header;

    public $content;



    public function convertToDTO($notificationTemplate){
        $notificationTemplateDto = new NotificationTemplateDTO();
        $notificationTemplateDto->setId($notificationTemplate->id);
        $notificationTemplateDto->setLabel($notificationTemplate->label);
        $notificationTemplateDto->setHeader($notificationTemplate->header);
        $notificationTemplateDto->setContent($notificationTemplate->content);
        return $notificationTemplateDto;
    }

    /**
     * @return mixed
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * @param mixed $content
     */
    public function setContent($content)
    {
        $this->content = $content;
    }

    /**
     * @return mixed
     */
    public function getHeader()
    {
        return $this->header;
    }

    /**
     * @param mixed $header
     */
    public function setHeader($header)
    {
        $this->header = $header;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param mixed $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }






}