class TaskDTO{


    public $id;

    public $label;

    public $serviceId;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param mixed $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }

    /**
     * @return mixed
     */
    public function getServiceId()
    {
        return $this->serviceId;
    }

    /**
     * @param mixed $serviceId
     */
    public function setServiceId($serviceId)
    {
        $this->serviceId = $serviceId;
    }

    public function convertToDTO($task,$serviceId=null){
        $taskDto = new TaskDTO();
        $taskDto->setId($task->id);
        $taskDto->setLabel($task->label);
        $taskDto->setServiceId($serviceId);
        return $taskDto;
    }


}