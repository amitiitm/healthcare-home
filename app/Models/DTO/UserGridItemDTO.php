class UserGridItemDTO{

    public $id;

    public $name;

    public $email;

    public $roles;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getRoles()
    {
        return $this->roles;
    }

    /**
     * @param mixed $roles
     */
    public function setRoles($roles)
    {
        $this->roles = $roles;
    }


    /**
     * @return mixed
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param mixed $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }



    public function convertToDTO($user){
        //d($user);
        $userMinimalDtoTemp = new UserGridItemDTO();
        $userMinimalDtoTemp->setId($user->id);
        $userMinimalDtoTemp->setName($user->name);
        $userMinimalDtoTemp->setEmail($user->email);

        return $userMinimalDtoTemp;
    }




}